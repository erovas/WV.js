namespace WV.Enums
{
    public enum PrintColorMode
    {
        /// <summary>
        /// The default color mode for a printer.
        /// </summary>
        Default = 0,

        /// <summary>
        /// Indicate that the printed output will be in color.
        /// </summary>
        Color = 1,

        /// <summary>
        /// Indicate that the printed output will be in shades of gray.
        /// </summary>
        Grayscale = 2
    }
}